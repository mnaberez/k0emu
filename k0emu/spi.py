class BaseSPITarget(object):
    """Base class for SPI target devices."""

    def spi_begin(self):
        """STB asserted (high) — start of SPI command."""
        raise NotImplementedError

    def spi_exchange(self, tx_byte):
        """Exchange one SPI byte.  Returns the byte to send back (MISO)."""
        raise NotImplementedError

    def spi_end(self):
        """STB deasserted (low) — end of SPI command, process it."""
        raise NotImplementedError


class UPD16432B(BaseSPITarget):
    """NEC uPD16432B LCD controller.

    Processes SPI command packets and updates internal RAM areas
    as the uPD16432B would.  Also provides key scan data.
    """

    def __init__(self):
        self.display_ram = bytearray(0x19)
        self.pictograph_ram = bytearray(0x08)
        self.chargen_ram = bytearray(7 * 0x10)
        self.led_ram = bytearray(1)
        self.key_data = bytearray(4)
        self.dirty = False

        self._cmd_buf = []
        self._current_ram = None
        self._address = 0
        self._increment = False
        self._display_pixels = None

    def spi_begin(self):
        self._cmd_buf = []

    def spi_exchange(self, tx_byte):
        self._cmd_buf.append(tx_byte)
        index = len(self._cmd_buf) - 1
        if index < len(self.key_data) and len(self._cmd_buf) > 0:
            if (self._cmd_buf[0] & 0xC7) == 0x44:
                return self.key_data[index]
        return 0x00

    def spi_end(self):
        if len(self._cmd_buf) == 0:
            return
        if (self._cmd_buf[0] & 0xC7) == 0x44:
            return
        self._process_command()

    def _process_command(self):
        cmd = self._cmd_buf[0]
        cmd_type = cmd & 0xC0
        if cmd_type == 0x00:
            self._process_display_setting()
        elif cmd_type == 0x40:
            self._process_data_setting()
        elif cmd_type == 0x80:
            self._process_address_setting()
        elif cmd_type == 0xC0:
            self._process_status()
        if self._current_ram is not None:
            for b in self._cmd_buf[1:]:
                self._write_data_byte(b)

    def _process_display_setting(self):
        pass

    def _process_data_setting(self):
        cmd = self._cmd_buf[0]
        mode = cmd & 0x07
        ram_table = {
            0: self.display_ram,
            1: self.pictograph_ram,
            2: self.chargen_ram,
            3: self.led_ram,
        }
        if mode in ram_table:
            self._current_ram = ram_table[mode]
        else:
            self._current_ram = None
            self._address = 0
        if mode in (0, 1):
            self._increment = not bool(cmd & 0x08)
        else:
            self._increment = True
            self._address = 0

    def _process_address_setting(self):
        address = self._cmd_buf[0] & 0x1F
        if self._current_ram is self.chargen_ram:
            if address < 0x10:
                self._address = address * 7
            else:
                self._address = 0
        elif self._current_ram is None:
            self._address = 0
        else:
            self._address = address
            self._wrap_address()

    def _process_status(self):
        pass

    def _write_data_byte(self, b):
        if self._address < len(self._current_ram):
            if self._current_ram[self._address] != b:
                self._current_ram[self._address] = b
                self.dirty = True
                if self._current_ram is self.display_ram or \
                   self._current_ram is self.chargen_ram:
                    self._display_pixels = None
        if self._increment:
            self._address += 1
            self._wrap_address()

    def _wrap_address(self):
        if self._current_ram is not None:
            if self._address >= len(self._current_ram):
                self._address = 0

    def get_display_pixels(self):
        """Render display RAM to pixels.  Returns 7 bytes per
        character position (len(display_ram) * 7 bytes total).
        Each byte is one row of a 5-wide character in the same
        format as the uPD16432B chargen RAM."""
        if self._display_pixels is None:
            n = len(self.display_ram)
            pixels = bytearray(7 * n)
            for i in range(n):
                char_code = self.display_ram[i]
                if char_code < 0x10:
                    src = self.chargen_ram
                    offset = char_code * 7
                else:
                    src = CHARSET_VW_PREMIUM_5
                    offset = char_code * 7
                pixels[i * 7:(i + 1) * 7] = src[offset:offset + 7]
            self._display_pixels = pixels
        return self._display_pixels


